# What is the default return value of to_s when invoked on an object? Where could you go to find out if you want to be sure?

# The default return value of the to_s method call on an object is the object itself, which includes its memory location and instance variables. Where to find more inforamation about this would be the documentation, specifically under the Object class http://ruby-doc.org/core/Object.html#method-i-to_s.

# by default the to_s method will return the objects class and an encoding of the object ID

