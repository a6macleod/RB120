# Which of the following are objects in Ruby? If they are objects, how can you find out what class they belong to?

# true
# "hello"
# [1, 2, 3, "happy days"]
# 142

# they are all objects. You can find out their class by calling #class on them

p true.class
p "hello".class
p [1, 2, 3, "happy days"].class
p 142.class
